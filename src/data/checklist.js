export const makeWorkerRow = (nombre) => ({
  nombre,
  bloque: "",
  criteria: Array(15).fill(null), // null=sin marcar, 1=cumple, 0=no cumple
  obs: "",
  firma: null,
});

export const makeReport = (finca, semana, year) => ({
  finca,
  semana: Number(semana),
  year:   Number(year),
  closed: false,
  areas: Object.fromEntries(
    Object.keys(WORKERS).map((k) => [
      k,
      {
        supervisor: "",
        jefe: "",
        rows: WORKERS[k].map(makeWorkerRow),
      },
    ])
  ),
});

export const getWeekNumber = () => {
  const d = new Date();
  const start = new Date(d.getFullYear(), 0, 1);
  return Math.ceil(((d - start) / 86400000 + start.getDay() + 1) / 7);
};

export const calcScore = (row) => {
  const c = row.criteria.filter((x) => x === 1).length;
  return { cumple: c, total: 15, pct: Math.round((c / 15) * 100) };
};
